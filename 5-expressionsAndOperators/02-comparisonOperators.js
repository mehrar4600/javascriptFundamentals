// comparison operators

// equal sign
'3'==3;  //looks at content

// strict equal sign
3 === 3; // looks at content and datatype

//not equal
'3' != 4;

//strict not equal
'3' !== 3;

//greater than
3>2

//less than
2<3

//less than or equal to
2 <= 3;

//greater than or equal to
3 >= 2;

//and
2 && 2;

//or
2 || 2;