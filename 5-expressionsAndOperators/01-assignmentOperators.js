//assignment operators
var x = 5; //equal sign is the assignmnet operator

//addition assignment operator
x += 1; // x =x+1

//subtraction asignment
x-= 1;// x = x-1

//multiplication assignment
x *= 1; // x = x*1

//division assignment
x /= 1; // x = x/1

//remainder or modulus assignment
x%= 1;// x =x%1;

//exponential asignment
x **= 1;// x = x ** 1 // power to x